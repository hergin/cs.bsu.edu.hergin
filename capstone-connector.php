            <h3>What is Capstone Connector?</h3>
            <p>
                Capstone Connector is an industry-agnostic center under <a href="http://innovationconnector.com/" target="_blank">Innovation Connector</a>.
                It aims to connect the local community partners with student capstone teams (for now only Computer Science) to solve partners' technological problems by building custom software.
                It is open to any company, startup, non-profit organization, faculty member, or individual.
            </p>


            <h3>Benefits of Capstone Connector</h3>
            
                Capstone Connector aims the collaboration to be beneficial for all stakeholders.
                <ul>
                    <li>The capstone teams benefit from working with a real partner.</li>
                    <li>The community partners benefit from having their technological ideas turning into software products.</li>
                    <li>The city has a chance to keep its bright talents to itself (what we call "Brain Retain").</li>
                    <li>The students have a chance to be hired immediately.</li>
                </ul>
            


            <h3>How does Capstone Connector work?</h3>
            <ol>
                <li>Community partners propose a project to the center.</li>
                <li>Director of Capstone Connector analyzes the project. He can request more information if needed. He has the experience to accept the projects as is, with small modifications, or after separating into a few projects. See <a href="#projectRequirements">Project Requirements</a> below.</li>
                <li>Capstone Connector assigns a student development team to the community partner.</li>
                <li>Community partner and student development teams work together by meeting at least once a month. At every meeting, student teams are responsible for creating and demonstrating a running software to their community partners.</li>
            </ol>


            <h3>Timeline of Capstone Connector projects</h3>
            <p>
                <em>Summer to October 1st:</em> Project/partner solicitation.<br/>
                <em>First week of October:</em> First meeting to collect project requirements.<br/>
                <em>November:</em> First iteration<br/>
                <em>December:</em> Second iteration<br/>
                <em>January:</em> Third iteration<br/>
                <em>February:</em> Fourth iteration<br/>
                <em>March:</em> Fifth (Final) iteration<br/>
                <em>March to April 30th:</em> Acceptance testing and preparation for maintenance<br/>
                <em>May 1st:</em> Sign off
            </p>


            <h3>When should a community partner give its project to Capstone Connector?</h3>
            As a community partner, you should think of proposing a project to Capstone Connector instead of hiring a software development company if one or more of the following is true:
                <ul>
                    <li>You don't have enough budget to hire professional software developers.</li>
                    <li>You don't want to deal with professional software developers.</li>
                    <li>You want to do something good for the local community through supporting the education of our students.</li>
                    <li>You want to reduce training costs of your potential future employees.</li>
                    <li>You have a side-project which you don't want to assign a budget.</li>
                    <li>You want to prototype a new feature of your already existing software.</li>
                    <li>You have an idea and wants to see how its implementation will look.</li>
                </ul>

            <h3 id="projectRequirements">Project requirements</h3>
            
            <p>Capstone Connector doesn't have strict requirements for the proposed projects. The only thing you need to be aware of is that we don't build business card-like static web pages. There are hundreds of online services that do this for no or low cost. We are developing custom software for your custom needs. It might be a full-stack application, a library, a mobile application, a web application, or any other software development. The community partner is free to select its technology stack for the projects. If not, the director and the team will choose the most suitable stack for the partner.
            </p>

            <h3>Cost of the projects</h3>
            <p>
                The accepted projects do not have any development cost billed to the partner. However, the partners are welcome to donate some amount to the center for the expenses of the student teams. These donations are used to cover the partner-student team meeting expenses (such as travelling) and any software licensing or cloud costs throughout the development.
            </p>


